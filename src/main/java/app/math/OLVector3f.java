package app.math;

public class OLVector3f {
    public float x;
    public float y;
    public float z;

    public OLVector3f() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    public OLVector3f(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
